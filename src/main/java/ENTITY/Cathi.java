package ENTITY;

public class Cathi {
    int idcathi;
    String giothi;

    public Cathi() {
    }

    public Cathi(int idcathi, String giothi) {
        this.idcathi = idcathi;
        this.giothi = giothi;
    }

    public int getIdcathi() {
        return idcathi;
    }

    public void setIdcathi(int idcathi) {
        this.idcathi = idcathi;
    }

    public String getGiothi() {
        return giothi;
    }

    public void setGiothi(String giothi) {
        this.giothi = giothi;
    }
}
